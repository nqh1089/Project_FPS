    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        try {
            int min = Integer.parseInt(txtGiaMin.getText());
            int max = Integer.parseInt(txtGiaMax.getText());

            List<SanPham> list = service.searchByGia(min, max);
            loadTable(list);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap so hop le cho min va max di sop ei");
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

