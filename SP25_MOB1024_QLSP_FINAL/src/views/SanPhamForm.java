    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SanPham sp = getFormData();
        if (sp != null) {
            boolean success = service.insert(sp);
            if (success) {
                JOptionPane.showMessageDialog(this, "Them thanh cong!");
                loadTable(service.getAll());
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Khong them duoc dau sop e!");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Co chac la sua khong sop?", "Chap nhan", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        SanPham sp = getFormData();
        if (sp != null) {
            if (sp.getTrangThai().equalsIgnoreCase("Het Hang")) {
                sp.setGiaBan(0);  //Khi het hang thi gia = 0
            }

            boolean success = service.update(sp);
            if (success) {
                JOptionPane.showMessageDialog(this, "Sua xong roi nhe!");
                loadTable(service.getAll());
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "Leu leu khong sua duoc :v");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

