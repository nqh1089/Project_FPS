    private void loadTable(List<SanPham> list) {
        model = (DefaultTableModel) tblSanPham.getModel();
        model.setRowCount(0);
        for (SanPham sp : list) {
            model.addRow(new Object[]{
                sp.getMa(),
                sp.getTen(),
                sp.getTrangThai(),
                sp.getGiaBan()
            });
        }
    }

    private SanPham getFormData() {
        try {
            int ma = Integer.parseInt(txtMa.getText());
            String ten = txtTen.getText().trim();
            int gia = Integer.parseInt(txtGiaBan.getText());
            String trangThai = rdConHang.isSelected() ? "Còn hàng" : "Hết hàng";

            if (ten.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Dang trong ten kia sop oi");
                return null;
            }

            return new SanPham(ma, ten, trangThai, gia);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap ma va gia la so");
            return null;
        }
    }

    private void clearForm() {
        txtMa.setText("");
        txtTen.setText("");
        txtGiaBan.setText("");
        rdConHang.setSelected(true);
        txtGiaMin.setText("");
        txtGiaMax.setText("");
    }

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {
        int row = tblSanPham.getSelectedRow();
        if (row != -1) {
            txtMa.setText(tblSanPham.getValueAt(row, 0).toString());
            txtTen.setText(tblSanPham.getValueAt(row, 1).toString());
            String trangThai = tblSanPham.getValueAt(row, 2).toString();
            txtGiaBan.setText(tblSanPham.getValueAt(row, 3).toString());

            if (trangThai.equalsIgnoreCase("Còn hàng")) {
                rdConHang.setSelected(true);
            } else {
                rdHetHang.setSelected(true);
            }
        }
    }

